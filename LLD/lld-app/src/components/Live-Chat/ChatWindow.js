function generateRandomNames() {
  return nameList[Math.floor(Math.random() * nameList.length)];
}

const ChatWindow = () => {
  const [messages, setMessages] = useState([]);

  const fetchData = () => {
    // Simulating API response
    const data = [
      {
        id: Math.random(),
        name: generateRandomNames(),
        message: `${generateRandomNames()} using live chat app !!`,
      },
    ];

    setMessages((prevMessages) => {
      let newMessageList = [...data, ...prevMessages];
      // âœ… Use slice instead of splice to keep the newest 100
      newMessageList = newMessageList.slice(0, CHAT_MESSAGES_LIMIT);
      return newMessageList;
    });
  };

  useEffect(() => {
    const interval = setInterval(fetchData, 1000);
    return () => clearInterval(interval);
  }, []);

  return (
    <div className="flex w-full h-[400px] border border-black m-5 overflow-y-scroll flex-col-reverse">
      {messages.length > 0 ? (
        messages.map((msg, i) => <ChatMessage key={i} {...msg} />)
      ) : (
        <p className="text-gray-500 text-center p-5">Loading messages...</p>
      )}
    </div>
  );
};

export default ChatWindow;
